export function generateDailyNewsForDate(date) {
  const yyyy = date.getFullYear();
  const mm = String(date.getMonth() + 1).padStart(2, '0');
  const dd = String(date.getDate()).padStart(2, '0');
  const dateStr = `${yyyy}-${mm}-${dd}`;
  const dayVal = date.getDate();

  const polIdx = (dayVal + 0) % dailyNewsPool.politics.length;
  const sciIdx = (dayVal + 1) % dailyNewsPool.science.length;
  const sptIdx = (dayVal + 2) % dailyNewsPool.sports.length;

  const polTpl = dailyNewsPool.politics[polIdx];
  const sciTpl = dailyNewsPool.science[sciIdx];
  const sptTpl = dailyNewsPool.sports[sptIdx];

  return [
    {
      ...polTpl,
      id: `ca-daily-politics-${dateStr}`,
      date: dateStr
    },
    {
      ...sciTpl,
      id: `ca-daily-science-${dateStr}`,
      date: dateStr
    },
    {
      ...sptTpl,
      id: `ca-daily-sports-${dateStr}`,
      date: dateStr
    }
  ];
}

